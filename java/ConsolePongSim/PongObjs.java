abstract class PongObjs {
	private int position;
	private int length;
	
	protected int getPosition() {
		return position;
	}
	protected void setPosition(int position) {
		this.position = position;
	}
	protected int getLength() {
		return length;
	}
	protected void setLength(int length) {
		this.length = length;
	}
	
}
